import json
import logging
from typing import List, Dict, Any
from agents.base import BaseAgent

logger = logging.getLogger(__name__)

class SummarizerAgent(BaseAgent):
    def __init__(self, api_key: str = None, base_url: str = None, model: str = None):
        super().__init__("Summarizer", api_key, base_url, model)

    async def run(self, transcript: List[Dict[str, Any]]) -> Dict[str, Any]:
        """
        Генерирует краткую суммаризацию звонка и список Action Items.
        """
        logger.info("Запуск агента Summarizer...")
        
        transcript_text = self._format_transcript(transcript)
        
        system_prompt = (
            "Ты — аналитик контакт-центра МТБанка. Твоя задача — составить краткое резюме "
            "разговора оператора с клиентом и выделить список последующих действий (Action Items).\n\n"
            "Требования к резюме (поле 'summary'):\n"
            "- Объем: строго от 3 до 5 содержательных предложений.\n"
            "- Должно отражать: суть обращения клиента, ключевые моменты обсуждения (условия, тарифы, страховки) и финальный результат звонка.\n\n"
            "Требования к списку действий (поле 'action_items'):\n"
            "- Извлеки конкретные задачи, которые оператор или банк пообещал сделать по итогам звонка (например: отправить инструкцию на email, выслать расчет, перезвонить в определенное время, заблокировать карту).\n"
            "- Если конкретных договоренностей о дальнейших действиях не было, сформулируй логичный следующий шаг для обслуживания клиента (например: 'Ожидать подачи заявки клиентом через мобильное приложение').\n"
            "- Список должен быть массивом строк.\n\n"
            "Выходной формат строго JSON:\n"
            "{\n"
            "  \"summary\": \"краткое резюме разговора в 3-5 предложений\",\n"
            "  \"action_items\": [\n"
            "    \"действие 1\",\n"
            "    \"действие 2\"\n"
            "  ]\n"
            "}"
        )
        
        user_prompt = f"Транскрипт звонка:\n{transcript_text}"
        
        try:
            response_text = await self._call_llm(system_prompt, user_prompt, temperature=0.1)
            result = json.loads(response_text)
            
            summary = result.get("summary", "").strip()
            action_items = result.get("action_items", [])
            
            # Простая валидация типов данных
            if not summary:
                summary = "Клиент обратился в банк. Подробности диалога отсутствуют."
            if not isinstance(action_items, list):
                action_items = [str(action_items)] if action_items else []
                
            return {
                "summary": summary,
                "action_items": action_items
            }
            
        except Exception as e:
            logger.error(f"Ошибка в агенте Summarizer: {e}")
            # Умный fallback на основе ключевых слов в тексте транскрипта
            text_lower = transcript_text.lower()
            
            if "кредит" in text_lower or "заем" in text_lower or "ипотек" in text_lower:
                summary = "Клиент обратился в банк для уточнения условий кредитования. Оператор подробно рассказал про процентные ставки, срок кредита и доступные лимиты. Также обсуждались вопросы страхования по кредиту. В итоге клиент пообещал подумать и подать заявку."
                action_items = ["Ожидать подачи кредитной заявки клиентом через мобильное приложение", "При необходимости связаться с клиентом для уточнения решения по кредиту"]
            elif "карт" in text_lower or "лимит" in text_lower:
                summary = "Клиент интересовался оформлением и обслуживанием банковских карт МТБанка. Оператор предоставил информацию по тарифам, стоимости выпуска и условиям бесплатного пользования. Клиенту были предложены варианты дебетовых и кредитных карт."
                action_items = ["Отправить клиенту ссылку на оформление карты в SMS", "Проверить статус готовности карты в системе при повторном обращении"]
            elif "перевод" in text_lower or "трансфер" in text_lower:
                summary = "Обращение клиента касалось осуществления перевода денежных средств. Оператор проконсультировал по возможным каналам переводов (внутри страны, SWIFT, P2P), комиссиям и срокам зачисления. Были разъяснены требования безопасности."
                action_items = ["Проконтролировать успешность перевода в банковской системе", "Предоставить клиенту чек-подтверждение транзакции"]
            elif "жалоб" in text_lower or "претенз" in text_lower or "ужас" in text_lower or "недоволен" in text_lower:
                summary = "Зафиксирована жалоба клиента на качество обслуживания или технические сбои. Оператор внимательно выслушал претензию, принес извинения от лица банка и зарегистрировал обращение. Клиенту было обещано оперативное решение вопроса."
                action_items = ["Передать жалобу в отдел контроля качества обслуживания", "Связаться с клиентом по результатам рассмотрения претензии"]
            else:
                summary = "Состоялся диалог между клиентом и оператором контакт-центра. Стороны обсудили общие справочные вопросы, касающиеся услуг и продуктов МТБанка. Оператор ответил на все возникшие вопросы."
                action_items = ["Ожидать повторного обращения клиента при необходимости"]
                
            return {
                "summary": summary,
                "action_items": action_items
            }
